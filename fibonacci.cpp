// variable int 

#include <iostream>
using namespace std;

int main() {
		int n;
		cin >> n;
    int a=0;
    int b=1;
    int c=0;
    if (n==1 || n==2){
      cout << endl << 1 << endl; 
    }else{
      for(int i=0;i<n;i++){
        c=a+b;
        a=b;
        b=c;
      }
        cout << endl << c << endl; 
    }
 
	return 0;
}

